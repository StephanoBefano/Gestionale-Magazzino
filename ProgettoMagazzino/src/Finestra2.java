    private void ModificaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModificaButtonActionPerformed
        int i = jTable1.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        if (i >= 0) {
            String codiceDaModificare = (String) model.getValueAt(i, 0);
            model.setValueAt(casellaNome.getText(), i, 1);
            model.setValueAt(casellaDescrizione.getText(), i, 2);
            model.setValueAt(casellaGiacenza.getText(), i, 3);
            model.setValueAt(casellaPrezzo.getText(), i, 4);

        String nuovoNome = casellaNome.getText();
        String nuovaDescrizione = casellaDescrizione.getText();
        int nuovaGiacenza = Integer.parseInt(casellaGiacenza.getText());
        double nuovoPrezzo = Double.parseDouble(casellaPrezzo.getText());

        magazzino.ModificaProdotto(codiceDaModificare, nuovoNome, nuovaDescrizione, nuovaGiacenza, nuovoPrezzo);
        } else {
            JOptionPane.showMessageDialog(null, "Nessun prodotto selezionato");
        }
    }//GEN-LAST:event_ModificaButtonActionPerformed


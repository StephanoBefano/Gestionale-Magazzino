    private void SalvaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SalvaButtonActionPerformed
        try {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        if (model.getRowCount() == 0) {
            JOptionPane.showMessageDialog(null, "Nessun dato da salvare", "Errore", JOptionPane.ERROR_MESSAGE);
            return;
        }

        BufferedWriter bw = new BufferedWriter(new FileWriter("dati_tabella.txt"));

        for (int row = 0; row < model.getRowCount(); row++) {
            for (int col = 0; col < model.getColumnCount(); col++) {
                bw.write(model.getValueAt(row, col).toString());
                bw.write("\t");
            }
            bw.newLine();
        }

        bw.close();

        JOptionPane.showMessageDialog(null, "Dati salvati con successo", "Salvataggio", JOptionPane.INFORMATION_MESSAGE);
    } catch (IOException e) {
        JOptionPane.showMessageDialog(null, "Errore durante il salvataggio dei dati", "Errore", JOptionPane.ERROR_MESSAGE);
    }//GEN-LAST:event_SalvaButtonActionPerformed
    }
    private void importaDaFile(File file) throws IOException {
        try (BufferedReader reader = new BufferedReader(new FileReader(file))) {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Pulisci la tabella prima di importare

        String line;
        while ((line = reader.readLine()) != null) {
            String[] data = line.split("\\t");

            if (data.length >= 5) {
                String codice = data[0];
                String nome = data[1];
                String descrizione = data[2];
                int giacenza = Integer.parseInt(data[3]);
                double prezzo = Double.parseDouble(data[4]);

                Prodotto prodotto = new Prodotto(codice, nome, descrizione, giacenza, prezzo);
                
                // Aggiunge l'oggetto Prodotto alla lista
                try {
                    magazzino.AggiungiProdotto(prodotto);
                } catch (IllegalArgumentException e) {
                    System.out.println(e.getMessage());
                }

                // Aggiunge la riga alla tabella
                model.addRow(data);
            }
        }
    }
    }
    
    private void CaricaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CaricaButtonActionPerformed
        try {
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Seleziona il file da cui importare");

        int userSelection = fileChooser.showOpenDialog(this);
        if (userSelection == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();
            importaDaFile(selectedFile);
            JOptionPane.showMessageDialog(null, "Dati importati con successo", "Importazione", JOptionPane.INFORMATION_MESSAGE);
        }
    } catch (IOException e) {
        JOptionPane.showMessageDialog(null, "Errore durante l'importazione dei dati", "Errore", JOptionPane.ERROR_MESSAGE);
        e.printStackTrace(); 
    }
    }//GEN-LAST:event_CaricaButtonActionPerformed

        

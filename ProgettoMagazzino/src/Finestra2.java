    private void AggiungiButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AggiungiButtonActionPerformed
        String C = casellaCodice.getText();
        String N = casellaNome.getText();
        String D = casellaDescrizione.getText();
        String G = casellaGiacenza.getText();
        String P = casellaPrezzo.getText();

        if (C.isEmpty() || N.isEmpty() ||  D.isEmpty() || G.isEmpty() || P.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Riempi tutte le caselle", "Errore", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int G1 = Integer.parseInt(G);
        double P1 = Double.parseDouble(P);

        if (contieneProdottoConCodice(C)) {
        JOptionPane.showMessageDialog(null, "Prodotto già esistente con questo codice", "Errore", JOptionPane.ERROR_MESSAGE);
        return;
    }
        Prodotto p = new Prodotto(C, N, D, G1, P1);
        try {
            magazzino.AggiungiProdotto(p);
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            Object[] row = {C, N, D, G1, P1};
            model.addRow(row);
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(null, "Prodotto già esistente con questo codice", "Errore", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_AggiungiButtonActionPerformed
    private boolean contieneProdottoConCodice(String codice) {
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    for (int row = 0; row < model.getRowCount(); row++) {
        Object valoreCella = model.getValueAt(row, 0);
        if (valoreCella != null && valoreCella.toString().equals(codice)) { 
            return true;
        }
    }
    return false;
}
    
